int definition_of_objects(vector<int> input_object) {
    int temp_difference = 102000;
    int difference = 0;
    int temp_index = 0;
    for (int i = 0; i < objects.size(); i++) {
        vector<int> temp = objects[i];
        for (int j = 0;j < 400; ++j) {
            difference += abs(input_object[j] - temp[j]);
        }
        if (difference < temp_difference) {
            temp_index = i;
            temp_difference = difference;
        }
        difference = 0;
    }
    return temp_index;
}

